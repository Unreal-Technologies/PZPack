BB_WhereAmI.CheckPlayerArea = function()
	local playerObj = getPlayer()
	local playerSq = playerObj:getSquare()

    for _, area in ipairs(BB_WhereAmI.areaDB) do
		if area.activated then
			if (playerSq:getX() >= area.startX and playerSq:getX() <= area.endX and playerSq:getY() >= area.startY and playerSq:getY() <= area.endY) then
				BB_WhereAmI.currentArea = area.name
				return
			end
		end
    end

    BB_WhereAmI.currentArea = ""
end

local function onGameStart()
	for _, area in ipairs(BB_WhereAmI.areaDB) do
		if not area.activated then
			if area.mod and getActivatedMods():contains(area.mod) then
				area.activated = true
			end
		end
    end
end

Events.OnGameStart.Add(onGameStart)